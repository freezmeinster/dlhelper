<h2>Sekilas tentang Aplikasi ini</h2>
</p><p>
Dewasa ini gerakan open source telah berkembang sangat pesat. Banyak diatara masyarakat di sekitar kita yang mulai menggunakan sistem operasi open source.
Legal dan murah mungkin adalah salah satu faktor yang menjadikan gerakan opensource tumbuh subur di lingkungan kita. Namun seperti di ketahui bersama bahwa 
system operasi open source kebanyakan (terutama turunan UNIX) memiliki sedikit kendala dalam hal ketergantungan Library.
</p>
<p>
Untuk dapat menjalankan sebuah aplikasi kita para pengguna sistem operasi turunan Unix diharuskan untuk mengunduh pustak-pustak system secara terpisah
.Memang saat ini sudah banyak aplikasi pendukung yang memudahkan kita untuk menginstall pustaka-pustaka tersebut ( sebagai contoh APT,YUM,EMARGE) secara langsung saat kita menginstall aplikasi utama.
Kekurangan nya adalah kita harus selalu terkoneksi dengan internet. 
</p>
<p>
Lalu bagai mana dengan rekan-rekan yang ingin ikut menginstall aplikasi dengan banyak pustaka-pustaka
pendukung namun tanpa koneksi internet. 
</p>
<p>
Untuk itu lah aplikasi ini dibuat. Aplikasi ini dapat membantu para penggunan sistem operasi Opensource yang ingin menginstall aplikasi dengan banyak pustaka-pustaka
pendukung namun kesulitan dalam hal koneksi internet. Rekan-rekan yang membutuhkan aplikasi cukup mencarinya di aplikasi ini, lalu aplikasi akan mencarikan pake 
yang dibutuhkan dan mencoba untuk mencarikan juga pustak-pustaka pendukung nya. Jadi rekan-rekan pada akhirnya akan mendapat sebuah paket yang berisi aplikasi dan pustaka-pustaka
pendukungnya secara lengkap. 
</p>